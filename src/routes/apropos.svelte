  <h2> Cookies </h2>

<p> Un « <em>cookie</em> » est une information que stocke un serveur
sur votre ordinateur lorsque vous visitez des pages de ce serveur. Un
cookie permet de vous suivre pendant votre visite. L’infrastructure de
notation automatisée CodeGradX n’utilise que des cookies de session
nommés <em>u</em>, <em>t</em> ou <em>c</em>. Comme ce sont des
cookies de session, ils disparaissent dès que vous quittez votre
navigateur. CodeGradX est ainsi conforme au
  <a href='https://www.cnil.fr/fr/comprendre-le-rgpd'>RGPD</a>. </p>

<p> Dès que vous vous identifiez auprès d’un serveur de la
  constellation CodeGradX, un cookie (le cookie nommé <em>u</em>) vous
est octroyé qui vous identifie auprès de tous les serveurs de la
constellation. Ce cookie permet aux serveurs de vous associer aux
informations détenues sur vous (votre historique notamment).
  Techniquement, c’est un équivalent léger d’un certificat X509. </p>

